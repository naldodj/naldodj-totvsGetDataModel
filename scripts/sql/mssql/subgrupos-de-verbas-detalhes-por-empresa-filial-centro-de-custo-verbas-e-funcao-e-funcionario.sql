  AND RD_CC BETWEEN !CCDE! AND !CCATE!
  AND RA_FILIAL BETWEEN !FILIALDE! AND !FILIALATE!
  AND ZY__CODIGO BETWEEN !GRUPODE! AND !GRUPOATE!
  AND RJ_FUNCAO BETWEEN !FUNCAODE! AND !FUNCAOATE!
